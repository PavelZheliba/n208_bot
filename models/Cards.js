// models/Cards.js
const mongoose = require('mongoose');
const Schema = mongoose.Schema;

const Dimensions = new Schema({
    width: { type: Number, default: null },
    height: { type: Number, default: null },
    length: { type: Number, default: null },
    isValid: { type: Boolean, default: true },
});

const Photos = new Schema({
    big: { type: String, default: '' },
    tm: { type: String, default: '' }
});
// //                 'techSize': '44',
// //                 'wbSize': '44',
// //                 'skus': [
// //                     '2041550106550',
// //                 ],
const Sizes = new Schema({
    techSize: { type: String, default: '' },
    wbSize: { type: String, default: '' },
    skus: [ { type: String, default: '' }],
});

const CardsSchema = new Schema({
    nmID: { type: Number, default: null, index: true }, // Добавил index для ускорения поиска
    imtID: { type: Number, default: null },
    nmUUID: { type: String, default: '' },
    subjectID: { type: Number, default: null },
    subjectName: { type: String, default: '' },
    vendorCode: { type: String, default: '' },
    brand: { type: String, default: '' },
    title: { type: String, default: '' },
    description: { type: String, default: '' },
    video: { type: String, default: '' },
    needKiz: { type: Boolean, default: true },
    photos: [{ type: Photos }],
    dimensions: { type: Dimensions },
    characteristics: [{ type: {} }],
    sizes: [{ type: Sizes }],
});

// ИЗМЕНЕНО: Используем module.exports для совместимости
module.exports = mongoose.model('Cards', CardsSchema);